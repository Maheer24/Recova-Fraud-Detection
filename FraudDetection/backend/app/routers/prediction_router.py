def _extract_report_id_from_pdf(pdf_bytes: bytes) -> str | None:
    reader = PdfReader(BytesIO(pdf_bytes))
    text_parts = []

    for page in list(reader.pages)[:2]:
        page_text = page.extract_text() or ""
        if page_text:
            text_parts.append(page_text)

    metadata = reader.metadata
    if metadata:
        text_parts.extend([str(value) for value in metadata.values() if value])

    full_text = "\n".join(text_parts)

    # Only treat PDFs generated by this app as verifiable PDFs.
    if "Fraud Detection Report" not in full_text:
        return None

    match = re.search(r"Report\s*ID\s*[:#]?\s*([0-9a-fA-F]{24})", full_text, re.IGNORECASE)
    if match:
        return match.group(1)

    return None


def _build_verify_url(report_id: str) -> str:
    base = VERIFY_BASE_URL.rstrip("/")
    if VERIFY_LINK_MODE == "path":
        return f"{base}/{report_id}"

    # query mode avoids 404 on static hosts without SPA rewrite rules.
    separator = "&" if "?" in base else "?"
    return f"{base}{separator}verify={report_id}"


def _build_qr_drawing(url: str, size: int = 90) -> Drawing:
    qr_code = qr.QrCodeWidget(url)
    bounds = qr_code.getBounds()
    width = bounds[2] - bounds[0]
    height = bounds[3] - bounds[1]
    drawing = Drawing(size, size, transform=[size / width, 0, 0, size / height, 0, 0])
    drawing.add(qr_code)
    return drawing


@router.post("/predict")
async def upload_file(file: UploadFile = File(...)):
    try:
        file_name = file.filename
        content = await file.read()

        # Read file
        if file.filename.endswith(("xlsx", "xls")):
            df = pd.read_excel(BytesIO(content))

        elif file.filename.endswith("csv"):
            df = pd.read_csv(StringIO(content.decode("utf-8")))

        else:
            return JSONResponse(
                content={"error": "Upload CSV or Excel file"}, status_code=400
            )

        # Accept common variants like "address" or extra spaces in header names.
        normalized_columns = {str(col).strip().lower(): col for col in df.columns}
        if "address" not in normalized_columns:
            return JSONResponse(
                content={
                    "error": "Missing required column: Address",
                    "received_columns": [str(col) for col in df.columns],
                },
                status_code=400,
            )

        address_col = normalized_columns["address"]

        addresses = df[address_col]

        df_features = df.drop(columns=[address_col])
        preprocessed_df = preprocess_input_data(df_features)

        # Predict
        prediction_df, normal_indices, fraud_indices = predict_transaction(
            model, preprocessed_df
        )

        prediction_df["uploaded_file"] = file_name

        prediction_df["Address"] = addresses.values
        # Store dataframe temporarily for download
        processed_df[file.filename] = prediction_df

        # Store in Supabase without blocking local prediction response.
        try:
            supabase.table("Transactions").insert(
                prediction_df.to_dict(orient="records")
            ).execute()
        except Exception as supabase_error:
            print(f"Supabase insert skipped: {supabase_error}")

        return {
            "filename": file.filename,
            "data": prediction_df.to_dict(orient="records"),
        }

    except UnicodeDecodeError:
        return JSONResponse(
            content={"error": "File encoding issue. Upload CSV or Excel"},
            status_code=400,
        )

    except Exception as e:
        return JSONResponse(content={"error": str(e)}, status_code=500)


@router.get("/download_report/{filename}")
async def download_report(filename: str):
    try:
        logger.info("[FLOW] download_report started filename=%s", filename)

        # Fetch data
        df = fetch_transactions(filename)
        selected_cols = [
            "Address",
            "Flag",
            "Sent tnx",
            "Received Tnx",
            "Unique Sent To Addresses",
            "Unique Received From Addresses",
            "total Ether sent",
            "total ether balance"
        ]

        df_table = df[selected_cols]

        # Count fraud vs normal transactions
        fraud_count = int((df["Flag"] == "Fraudulent").sum())
        normal_count = int((df["Flag"] == "Normal").sum())

        # Prepare report data for MongoDB
        report_data = {
            "filename": filename,
            "summary": {
                "total_transactions": len(df),
                "fraud_count": fraud_count,
                "normal_count": normal_count,
                "fraud_percentage": round((fraud_count / len(df) * 100) if len(df) > 0 else 0, 2)
            },
            "transactions": df_table.to_dict(orient="records"),
            "columns": selected_cols
        }

        # Save report to MongoDB
        save_result = report_service.save_report(
            filename=filename,
            report_data=report_data,
            fraud_count=fraud_count,
            normal_count=normal_count
        )
        logger.info("[FLOW] Mongo save result=%s", save_result)

        anchor_result = {"success": False, "error": "Report not saved, blockchain anchor skipped"}
        if save_result.get("success"):
            if ENABLE_BLOCKCHAIN_ANCHOR:
                logger.info(
                    "[FLOW] Anchoring hash on Polygon report_id=%s hash=%s",
                    save_result.get("report_id"),
                    save_result.get("report_hash"),
                )
                anchor_result = blockchain_service.anchor_report(
                    save_result["report_id"],
                    save_result["report_hash"],
                )
                logger.info("[FLOW] Polygon anchor result=%s", anchor_result)
            else:
                anchor_result = {
                    "success": False,
                    "error": "Blockchain anchoring disabled (ENABLE_BLOCKCHAIN_ANCHOR=false)",
                }
                logger.warning(
                    "[FLOW] Blockchain anchoring disabled. Skipping on-chain anchor report_id=%s",
                    save_result.get("report_id"),
                )
        else:
            logger.warning("[FLOW] Skipping blockchain anchor because Mongo save failed")

        # Continue with PDF generation
        buffer = BytesIO()
        doc = SimpleDocTemplate(buffer, pagesize=landscape(letter))
        styles = getSampleStyleSheet()
        elements = []
        report_id = save_result.get("report_id", "")
        verify_url = _build_verify_url(report_id) if report_id else ""

        #  TITLE -----------------
        elements.append(Paragraph("Fraud Detection Report", styles["Title"]))
        elements.append(Spacer(1, 20))
        elements.append(Paragraph(f"Report ID: {report_id}", styles["Heading2"]))
        elements.append(Paragraph(f"Report Hash: {save_result.get('report_hash', '')}", styles["Normal"]))
        if verify_url:
            elements.append(Spacer(1, 8))
            elements.append(Paragraph("Scan QR to verify this report online:", styles["Normal"]))
            elements.append(Paragraph(verify_url, styles["Normal"]))
            elements.append(Spacer(1, 8))
            elements.append(_build_qr_drawing(verify_url, size=96))
        elements.append(Spacer(1, 18))

        #  TABLE -----------------
        elements.append(Paragraph("Predicted Transactions", styles["Heading2"]))
        elements.append(Spacer(1, 10))
        table_data = [df_table.columns.tolist()] + df_table.values.tolist()
        table = Table(table_data, repeatRows=1)
        table.setStyle(TableStyle([
            ("GRID", (0,0), (-1,-1), 0.5, colors.grey),
            ("BACKGROUND", (0,0), (-1,0), colors.lightgrey),
            ("ALIGN", (0,0), (-1,-1), "CENTER"),
            ("FONTSIZE", (0,0), (-1,-1), 7)
        ]))
        elements.append(table)
        elements.append(PageBreak())

        # NORMAL CHARTS -----------------
        elements.append(Paragraph("Insights Charts", styles["Heading1"]))
        elements.append(Spacer(1, 20))
        df_charts = df.drop(columns=["Address"]) if "Address" in df.columns else df

        chart_functions = [
            pie_chart,
            avg_time_between_sent_histogram,
            ether_sent_histogram,
            sent_received_bar_chart,
            unique_sent_addresses_boxplot
        ]

        for chart_func in chart_functions:
            chart_fig_dict = chart_func(df_charts)
            if isinstance(chart_fig_dict, str):
                chart_fig_dict = json.loads(chart_fig_dict)
            chart_fig_dict = sanitize_plotly_fig(chart_fig_dict)
            img_bytes = plotly_to_png(json.dumps(chart_fig_dict))
            add_image_to_pdf(elements, img_bytes.getvalue())

        elements.append(PageBreak())

        # XAI CHARTS -----------------
        elements.append(Paragraph("Explainable AI Analysis", styles["Heading1"]))
        elements.append(Spacer(1, 20))
        shap_val = compute_shap_val(model, df_charts)

        fraud_indices = df_charts[df_charts["Flag"] == "Fraudulent"].index
        normal_indices = df_charts[df_charts["Flag"] == "Normal"].index

        # SHAP bar plot
        bar_plot_base64 = xai_service.bar_plot(shap_val)
        img_bytes = decode_base64_image(bar_plot_base64)
        elements.append(Paragraph("Feature Importance", styles["Heading2"]))
        add_image_to_pdf(elements, img_bytes)

        # Fraud waterfall plots
        elements.append(Paragraph("Fraud Transaction Explanations", styles["Heading2"]))
        elements.append(Spacer(1, 10))
        fraud_waterfalls = xai_service.waterfall_plot(shap_val, fraud_indices)
        for img_str in fraud_waterfalls:
            img_bytes = decode_base64_image(img_str)
            add_image_to_pdf(elements, img_bytes, width=450, height=300, spacer=15)

        elements.append(PageBreak())

        # Normal waterfall plots
        elements.append(Paragraph("Normal Transaction Explanations", styles["Heading2"]))
        elements.append(Spacer(1, 10))
        normal_waterfalls = xai_service.waterfall_plot(shap_val, normal_indices)
        for img_str in normal_waterfalls:
            img_bytes = decode_base64_image(img_str)
            add_image_to_pdf(elements, img_bytes, width=450, height=300, spacer=15)

        # Build PDF
        doc.build(elements)
        buffer.seek(0)

        base_name = os.path.splitext(filename)[0]
        response_headers = {
            "Content-Disposition": f'attachment; filename="{base_name}_report.pdf"',
            "X-Report-Id": save_result.get("report_id", ""),
            "X-Report-Hash": save_result.get("report_hash", ""),
            "X-Verify-Url": verify_url,
            "X-Blockchain-Status": "anchored" if anchor_result.get("success") else "failed",
            "X-Blockchain-Tx-Hash": anchor_result.get("tx_hash", ""),
        }

        if not anchor_result.get("success"):
            response_headers["X-Blockchain-Error"] = anchor_result.get("error", "unknown error")
            logger.warning(
                "[FLOW] download_report finished with blockchain failure filename=%s error=%s",
                filename,
                anchor_result.get("error", "unknown error"),
            )
        else:
            logger.info(
                "[FLOW] download_report completed filename=%s tx_hash=%s",
                filename,
                anchor_result.get("tx_hash", ""),
            )

        return StreamingResponse(
            buffer,
            media_type="application/pdf",
            headers=response_headers
        )

    except ValueError as ve:
        logger.exception("[FLOW] download_report failed with value error filename=%s", filename)
        return JSONResponse({"error": str(ve)}, status_code=404)
    except Exception as e:
        logger.exception("[FLOW] download_report failed with unexpected error filename=%s", filename)
        return JSONResponse({"error": str(e)}, status_code=500)


@router.post("/verify_pdf")
async def verify_pdf(file: UploadFile = File(...)):
    try:
        pdf_bytes = await file.read()
        if not pdf_bytes:
            return JSONResponse({"success": False, "error": "Empty PDF file"}, status_code=400)

        report_id = _extract_report_id_from_pdf(pdf_bytes)
        if not report_id:
            return JSONResponse(
                {
                    "success": False,
                    "error": "Could not find a report ID in the PDF. Upload a PDF generated by this app.",
                },
                status_code=400,
            )

        result = report_service.verify_report_integrity(report_id, blockchain_service)
        result["source"] = "pdf"
        result["extracted_report_id"] = report_id

        if not result.get("success") and result.get("error"):
            error_text = (result.get("error") or "").lower()
            if "not found" in error_text:
                return JSONResponse(result, status_code=404)
            if "invalid" in error_text:
                return JSONResponse(result, status_code=400)
            return JSONResponse(result, status_code=200)

        return JSONResponse(result, status_code=200)
    except Exception as e:
        logger.exception("[VERIFY_PDF] Failed to verify PDF")
        return JSONResponse({"success": False, "error": str(e)}, status_code=500)
    

@router.get("/download_csv")
async def download_csv(filename: str):
    try:
        if filename not in processed_df:
            return JSONResponse(
                content={"error": "No processed file found with this name"},
                status_code=404,
            )

        df = processed_df[filename]

        output = StringIO()
        df.to_csv(output, index=False)
        output.seek(0)

        base_name = os.path.splitext(filename)[0]
        download_name = f"{base_name}_predicted.csv"

        return StreamingResponse(
            output,
            media_type="text/csv",
            headers={"Content-Disposition": f'attachment; filename="{download_name}"'},
        )

    except Exception as e:
        return JSONResponse(content={"error": str(e)}, status_code=500)


